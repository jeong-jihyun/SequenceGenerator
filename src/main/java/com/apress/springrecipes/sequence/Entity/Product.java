package com.apress.springrecipes.sequence.Entity;

import lombok.Getter;
import lombok.Setter;

@Setter
@Getter
public abstract class Product {
    private String name;
    private double price;

    public Product() {
    }

    public Product(String name, double price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String toString() {
        return "Product{" +
                "name='" + name + '\'' +
                ", price=" + price +
                '}';
    }
}
